  <p><font color="0000ff"><b>Peter Douglas</b></font> (&quot;Texas and Tom&quot;) - Peter is
  excited to be making his Ram's Head debut in the 1998 Winter One Acts production of
  &quot;Texas and Tom.&quot; This is Peter's first year at Stanford and only his second
  theatrical production. Stanford theatre-goers might recognize him from <u>Twelfth Night</u>,
  in which Peter played Valentine and a very dangerous-looking thug. Peter's most recent
  work includes the title role in <u>Hamlet</u> at Interlochen Center for the Arts in
  Michigan, Martin Dyshart in <u>Equus</u> at the National Cathedral School in Washington,
  D.C., and Stuckley in <u>The Castle</u> at the British American Drama Academy in Oxford,
  England. Peter is looking forward to working with Ram's Head again in future productions
  and would like to thank his family for all their support over the years. </p>
