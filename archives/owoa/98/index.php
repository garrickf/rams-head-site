<h2>Show Synopses</h2>
</a>

<blockquote>
  <font color="ff0000"><b><p>&quot;Texas and Tom&quot;</b></font>, written by Ilana Weiner
  and directed by Erryn Lewis, is the brilliant story of Tennessee Williams and Margo Jones,
  the amazing woman who helped to make his plays a success. The story of their relationship
  is told by mirroring the events and themes of &quot;The Glass Menagerie.&quot; It is a
  show about fast paced lifestyles, quick talking, and the road to success. The play crosses
  boundaries of time, space, and genre which make it visually appealing and exciting to
  watch. </p>
  <p><font color="ff0000"><b>&quot;Solitaire for Two&quot;</b></font>, written by Jeremy
  Sawyer and directed by Ana-Catrina Savulescu, is about solitude and solidarity, blurry
  dilemmas of fantasy and reality, and living with the death of love. It is a struggle in
  the face of ultimate mystery. Soren goes to bed with his nagging wife, Margaret. He wakes
  the next morning to discover a mysterious and beautiful woman whom he has never seen
  before in his bed. Things escalate from there. Meanwhile, in another world, an old man
  named Fred and a younger Bill play solitaire with their own decks and talk of love,
  spirits, and perverse dreams. </p>
  <p><font color="ff0000"><b>&quot;Shakespeare's Sonnets&quot;</b></font>, written by Samuel
  Park and directed by Dan Pintauro, follows 50 years in the lives of two gay men, together
  and apart, as they live through the oppressive McCarthy years and discover a world of
  pain, love, and desire. It's about the moments that we remember, and the things we can't
  forget. It's 1948 and Jean is discovering in college the things that he likes best:
  theatre, Shakespeare's sonnets, and his friend Adam. These are also the things that, for
  the rest of his life, will keep his heart afloat, and ablaze. </p>
</blockquote>

