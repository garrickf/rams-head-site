<h2>Show Synopses</h2>
</a>

<p>Fred Savage from television's Wonder Years makes his Directorial debut with <b>&quot;Connection
Down.&quot;</b> Written by Jon Goldman, the one-act explores identity and human connection
in the Internet era. Eric Toscano, who plays Barry, makes his first appearance on the
stage. Returning to Original Winter One Acts are Anne Cunha (Sheila) and Nathan Harper
(Jack). </p>

<p><b>&quot;Waiting for 'Waiting for Godot'&quot;</b> (written by Rudolph Delson, directed
by Jason Alisharan) is a comedy playing on the play &quot;Waiting for Godot.&quot; On the
thought that more in the theatre is in need of mockery than just Beckett, the play also
lampoons &quot;Rosencrantz and Guildenstern&quot;, Quentin Tarrantino, Jerry Seinfeld, the
playwright Rudolph Delson, and necrophelia. And what better way to mock theatre based on
males exchanging witty banter than by employing males engaged in witty banter? </p>

<p>In <b>&quot;The Voluntary Inane,&quot;</b> (written by Robin Moroney with Alex Sepiol,
directed by Arti Garg) Stanley Carpenter, a revered poet, is beginning to fray around the
edges. He suspects he is going insane, the healing power that poetry used to have for him
has worn thin. Given over to mad delusions he throws his girlfriend out of his house and
effectively disappears. The next time anybody sees him, he has renounced poetry and
instead spends his time making advertisements for a chicken company and writing for
birthday cards. His friends, appalled, try to convince him to take up his poetry again
rather than continue what they consider to be idiotic behaviour. The play is darkly comic
and also feature a chorus made up of neurotic members of the artistic community. </p>

