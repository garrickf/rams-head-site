<h3>STANFORD STUDENTS PRODUCE ORIGINAL SHOW</h3>

<p>PALO ALTO --<br>
Stanford University's student-run Ram's Head Theatrical Society announces its 1997
production of Original Winter One Acts. The show features three student-written and
directed one-act plays performed on the same evening. This year's show will also include
live musical performances. </p>

<p>Tickets can be purchased in White Plaza on campus 11-2 pm Monday through Friday or at
the box office at the Nitery Theatre in Stanford's Old Union which opens 30 minutes prior
to performances. Showtimes: January 16, 17, 21, 22, 23 at 8:00 pm; Friday, January 24 two
shows at 7:00 pm and 10:30 pm; and Saturday January 25 at 7:00 pm. Please call (415)
723-0801 for ticket reservations ($8 students, $15 general) and more information. The show
is expected to sell out. </p>

<p>The show's popularity can be attributed partly to the talented writers, directors and
actors involved. Fred Savage, from television's Wonder Years, will be making his
Directorial debut with &quot;Connection Down&quot; written by Jon Goldman. Producer Jodi
Rothman explains, &quot;I liked this script's innovative exploration of human connection
in the Internet era.&quot; </p>

<p>Three scripts were selected out of 18 original submissions. In addition to
&quot;Connection Down,&quot; Rudolph Delson's &quot;Waiting for 'Waiting for Godot'&quot;
directed by Jason Alisharan and Robin Moroney's &quot;The Voluntary Inane&quot; directed
by Arti Garg promise to be very entertaining. </p>

