<h3><u>Synopses</u></h3>

<p><b><cite>Petersburg</cite></b> is the story of an apartment manager
who must confront his family and friends after the death of a famous piano-playing tenant.
Set in Russia, this play from junior <b>James Copeland</b> explores the borderless issues
of responsibility, the meaning of true friendship, and the perception of life's worth.
Sophomore director <b>Kingsley Willis</b> has led an all-star cast towards a truly
memorable and amazing dramatic production!</p>

<p></a><b><cite>Nine Night Stand</cite></b> is the debut of <b>Scenic
Root,</b> a new improvisation group composed Stanford Improvisor and Sponge members and
dedicated to the creation of &quot;longform&quot; improvised scenarios. With the help of
audience suggestions, this troupe will create a new play right before your very eyes, so
every night will be a new experience. &quot;Hmmm...&quot;, you hear yourself thinking,
&quot;Very innnnnteresting.&quot; </p>

<p></a><b><cite>Dice</cite></b> asks how you would survive the pain of
kidnapping, the torture of beatings, the humiliation of interrogation, and the fear of
eternal imprisonment. Written by junior <b>Mark Ganek</b> and directed by senior <b>Alice
May Bugman,</b> this dark and troubling drama follows two strangers as they are forced to
jointly ask these questions...and realize that they do not like all the answers. You don't
want to miss this thrilling theatrical experience!</p>



