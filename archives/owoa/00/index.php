<h3 align="center">OWOA 2000 presents:</h3>

<p><br>
<b>Between Cleanings</b><br>
Written by Art Schram <br>
Directed by Avery T Willis<br>
<br>
In the public restrooms at the Chez Rufus restaurant, no one can escape the calls of
Nature, Fate and Truth. Between Cleanings is a one act play which exposes dirty dealings
at an office party.<br>
<br>
<br>
<b>The Christmas Feast</b><br>
Written by Andrew Hinderaker <br>
Directed by Jessica Donaghy<br>
<br>
This is a powerful story told by a young writer, who relates his painful memory of a
Christmas past spent with his caring mother and abusive, alcoholic father. The one-act
chronicles the mother and son's struggle, as they escape to find a new sense of strength
and family love.<br>
<br>
<br>
<b>Memento Mori</b><br>
Written by Paul Kerschen <br>
Directed by Geoff von Oeyen &amp; Jennifer Gadda<br>
<br>
Adrian is a young man who, for the past year, has been haunted by Death, a robed figure
that follows him everywhere, hogs the easy chair, and keeps him from getting dates. A
visit to the office of Doctor Omnibus, licensed psychiatrist, prompts a round of therapy
involving Adrian's father and a battery of drugs with unpronounceable names. These drugs
are provided by Ruby, the secretary with a master's degree, who flippantly deals with
Adrian's medications, appointments, and romantic advances. Gerbils are involved, but only
by implication. </p>

